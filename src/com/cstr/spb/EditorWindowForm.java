    public void saveTextTemplate(PostTemplate pt){
        PostTemplateLine[] selLines = selectedTemplate.lines;
        pt.lines = new PostTemplateLine[lineList.getModel().getSize()];
        
        for (int i = 0; i < pt.lines.length; i++){
            pt.lines[i] = new PostTemplateLine();
            pt.lines[i].lineText = lineList.getModel().getElementAt(i);
            if (selectedTemplate != null && i < selLines.length){
                pt.lines[i].allTextUpper = selLines[i].allTextUpper;
            }
        }
        
    }
    public void selectTextTemplate(PostTemplate pt){
        ((DefaultListModel) lineList.getModel()).removeAllElements();
        for (PostTemplateLine pl : pt.lines){
            if (pl.lineText.isEmpty()){
                continue;
            }
            ((DefaultListModel<String>) lineList.getModel()).add(lineList.getModel().getSize(), pl.lineText);
        }
        selectedTemplate = pt;
    }
