    public void updateTemplates(){
        updateTextTemplates();
        updateImageTemplates();
        updateShirtTemplates();
    }
    public void updateImageTemplates(){
        templateChooser.removeAllItems();
        for (ImageTemplate i : Main.iGenerator.templates){
            templateChooser.addItem(i.templateName.isEmpty() ? "untitled template" : i.templateName);
        }                
    }
    public void updateTextTemplates(){
        textTemplateChooser.removeAllItems();
        for (PostTemplate p : Main.pLibrary.postTemplates){
            textTemplateChooser.addItem(p.templateName.isEmpty() ? "untitled template" : p.templateName);
        }
    }
    public void updateShirtTemplates(){
        shirtTemplateSelect.removeAllItems();
        for (ShirtTemplateObject p : Main.iGenerator.shirtTemplates){
            shirtTemplateSelect.addItem(p.shirtTemplateName.isEmpty() ? "untitled template" : p.shirtTemplateName);
        }
    }
    
    public void selectTemplateObject(ImageTemplateObject i){
        objectTypeSelector.setSelectedIndex(i.templateObjectType - 1);
        outlineTypeSelector.setSelectedIndex(i.outlineMode - 1);
        
        colourCanBeRandom.setSelected(i.useRandomColour);
        useReplaceToken.setSelected(i.useToken);
        replaceTokenName.setText(i.tokenName);
        typeRFolder.setSelected(i.imageRandom == true);
        typeROnline.setSelected(i.imageRandom == false);
        textColourChoose.setBackground(i.preferredColour);
        outlineColourChoose.setBackground(i.preferredOutlineColour);
    }
    
    public void setSelectedShirt(ShirtTemplateObject sTO){
        backgroundImageTB1.setText(sTO.imageFileName);
    }
